package org.hibernate.tool.ide.completion;

/**
 * @author Dmitry Geraskov
 *
 */
public abstract class City {
	
	private Long id;
    
    private String name;
    
    public City() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
